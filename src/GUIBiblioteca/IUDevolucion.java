    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       
        String res=null;
        String filtro=this.txtbuscar.getText();
        
        Lector lec=null;
        
        if (estado==1){
        lec= IULector.oLe.buscarLecID(filtro);
        }else{
        lec= IULector.oLe.buscarLecDNI(filtro);
        }
        if(lec!=null) {
        
            res=lec.getNom() + " " + lec.getApe();
            DefaultTableModel datos = new DefaultTableModel();
            
            JTable tabla = new JTable(datos);
            datos.addColumn("Lector");
            datos.addColumn("Libro");
            datos.addColumn("Fecha");
            String fila[];
      
            pre=IUPrestamo.oPr.buscarPreLec(res);
            
            indicep=IUPrestamo.oPr.obtenerIndice(pre);
                if (pre != null){
                        fila =new String [3];
                        fila[0]=pre.getLector();
                        lect=pre.getLector();
                        fila[1]=pre.getLibro();
                        libr=pre.getLibro();
                        fila[2]=pre.getFecha();
                        datos.addRow(fila);
                        tabla.updateUI();
                }              
            tblRes.setModel(datos);
        }else{
            this.txtbuscar.setText("0 Resultados");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

