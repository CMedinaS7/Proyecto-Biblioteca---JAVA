     public void Iniciar(){
     
        int j=1;
   
        while ( j <= IUPersonal.oPe.obtenerCantidad() ) {
            Personal per=null;
            per= IUPersonal.oPe.buscarPerCod(String.valueOf(j));
            if(per!=null) {
                empleado.add(per.getNom() + " " + per.getApe());
            }
            j++;
        }
        this.txtPersonal.setItems(empleado);
        this.tblRes.setModel(new DefaultTableModel());
    }
     
