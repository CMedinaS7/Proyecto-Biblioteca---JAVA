public void BuscarXcodigo(String codigo){
    
        Lector lec=null;
                
        lec= oLe.buscarLecCod(codigo);
        
        if(lec!=null) {
            //Obtiene el indice del Obj. Curso almacenado en la coleccion(me va a servir luego para modificar)
            indice= oLe.obtenerIndice(lec);
            //Coloca en los componentes los datos extraidos de la Coleccion
            this.txtApellidos.setText(lec.getApe());
            this.txtDNI.setText(lec.getDni());
            this.txtDireccion.setText(lec.getDir());
            this.txtFecha.setText(lec.getFing());
            this.txtID.setText(lec.getId());
            this.txtNombres.setText(lec.getNom());
            this.txtTelefono.setText(lec.getTel());
            this.txtcodigo.setText(lec.getCodigo());
            this.btnGuardar.setEnabled(false);
            this.btnNuevo.setEnabled(true);
            this.btnEditar.setEnabled(true);
            boton=2;
            accion='E';
            bola=lec.getActivo();
            Verificar(bola);
            this.txtbuscar.setText("Ingrese ID!");
           }else{
            this.txtbuscar.setText("No Existe!!!");
        }
        this.Actualizar();
}

