    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        //Variables propias del metodo
        String codigo,nom,ape,dir,fing,dni,id,tel;
        int activo;
        boolean ex;
        codigo=this.txtcodigo.getText();
        nom=this.txtNombres.getText();
        ape=this.txtApellidos.getText();
        dir=this.txtDireccion.getText();
        fing=this.txtFecha.getText();
        dni=this.txtDNI.getText();
        id=this.txtID.getText();
        tel=this.txtTelefono.getText();
        activo=1;
  
        //Creacion del Obj
        Lector l = new Lector(codigo,nom,ape,dir,fing,dni,id,tel,activo);

        //***************************Guardar Empleado******************************
        if(accion=='N') {
            ex= oLe.agregarLector(l); //Guarda el obj. en la Coleccion
            if(ex==true) {
                IUMsg msg=new IUMsg();
                msg.setVisible(true);
                DesactivarCajas();
                this.btnGuardar.setEnabled(false);
                this.btnEditar.setEnabled(false);
                this.btnNuevo.setEnabled(true);
            }
            else{
                 IUMsg1 msg1=new IUMsg1();
                 msg1.setVisible(true);
        }}

        //***************************Modifica Empleado*****************************
        if(accion=='E') {
            ex= oLe.modificarLec(indice, l); //Modifica el Obj. en la Coleccion con el indice y el nuevo obj.
            if(ex==true) {
                IUMsg msg=new IUMsg();
                msg.setVisible(true);
                DesactivarCajas();
                this.btnGuardar.setEnabled(false);
                this.btnEditar.setEnabled(false);
                this.btnNuevo.setEnabled(true);
            }
            else
            {
             IUMsg1 msg1=new IUMsg1();
            msg1.setVisible(true);

            }
        }
            this.BuscarXcodigo("1");
        
    }//GEN-LAST:event_btnGuardarActionPerformed

